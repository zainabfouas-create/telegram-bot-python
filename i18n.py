def t(lang: str, key: str, *args) -> str:
    trans = TRANSLATIONS.get(lang, TRANSLATIONS["ar"])
    text = trans.get(key, TRANSLATIONS["ar"].get(key, key))
    if callable(text):
        return text(*args)
    return str(text)


def delivery_type_label(dtype: str, lang: str = "ar") -> str:
    return t(lang, "deliveryManual" if dtype == "manual" else "deliveryAuto")


def order_status_label(status: str, lang: str = "ar") -> str:
    mapping = {
        "completed": "statusCompleted",
        "pending": "statusPending",
        "refunded": "statusRefunded",
        "cancelled": "statusCancelled",
    }
    key = mapping.get(status)
    return t(lang, key) if key else status


def escape_html(text: str) -> str:
    return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
